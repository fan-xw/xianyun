<template>
  <el-form :model="form" 
           ref="form" 
           :rules="rules" 
           class="form">
            <el-form-item class="form-item" prop="username">
                <el-input placeholder="用户名手机" v-model="form.username">
                </el-input>
            </el-form-item>

            <el-form-item class="form-item" prop="captcha">
