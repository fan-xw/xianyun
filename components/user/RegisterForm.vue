                <el-input placeholder="密码" 
                          type="password"
                          v-model="form.password">
                </el-input>
            </el-form-item>

            <el-form-item class="form-item" prop="checkPassword">
                <el-input placeholder="确认密码" type="password" v-model="form.checkPassword">
                </el-input>
            </el-form-item>

            <el-button class="submit" type="primary" @click="handleRegSubmit">
                注册
            </el-button>
        </el-form>
</template>

<script>
// import dist from 'vuex-persistedstate';
export default {
  data () {
    // 由于校验函数只是在 data 使用,没必要写在methods方法里面
    // 校检确认密码
    const validateCheckPass = (rule,value,callback) => {
      if (!value) {
        callback(new Error('请再次输入密码'))
      } else if (value != this.form.password) {
        callback(new Error('两次输入的密码请保持一致'))
      } else {
        callback()
      }
    }

    return {
      // 表单数据
      form: {
        username: "",   // 登录用户名/手机
        password: "",    // 登录密码
        checkPassword: "", // 确认密码
        nickname: "",	// 昵称
        captcha: "",		// 手机验证码
      },
      // 🌱🌱1.验证规则
      rules:{
        username: [
          {
            required:true,
            message:'请输入用户名/手机号码',
            trigger:'blur'
          }
        ],   
        password: [
          {
            required:true,
            message:'请输入密码',
            trigger:'blur'
          }
        ],  
        // 确认密码的规则验证
        checkPassword:[
          {
            // 这里的校验并非默认自带的校验方式
            // 而是需要自定义,所以 判断标准和提示语都要自己写
            // 给一个函数作为 valdator 即可
            validator:validateCheckPass,
            trigger:'blur'
          }
        ],
        nickname: [
          {
            required:true,
            message:'请输入昵称',
            trigger:'blur'
          }
        ],
        captcha: [
          {
            required:true,
            message:'请输入手机验证码',
            trigger:'blur'
          }
        ],	
      }
    }
  },

  methods: {
    // 发送验证码
